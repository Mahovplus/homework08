first = input('Введите первое число: ')
second = input('Введите второе число: ')
third = input('Введите третье число: ')
if first == second == second == third:
    print(3)
elif first == second:
    print(2)
elif third == first:
    print(2)
elif second == third:
    print(2)
else:
    print(0)


